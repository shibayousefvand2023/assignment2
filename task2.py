if op == 'tan':
    num1 = int(input('Enter num1:'))
    result = tan , num1
print(math, tan(math.radians(num1)))
if op == 'cot':
    num1 = int(input('Enter num1:'))
    result = 1/tan , num1
print(math, 1/tan(math.radians(num1)))
if op == '!':
    num1 = int(input('Enter num1:'))
    result = math.factorial, num1
